// Функция для получения случайного цифрового вопроса
function getRandomNumericQuestion() {
    const randomIndex = Math.floor(Math.random() * numericQuestions.length);
    return {
        ...numericQuestions[randomIndex],
        type: 'numeric'
    };
}

// Функция для получения случайного вопроса с вариантами
function getRandomMultipleChoiceQuestion() {
    const randomIndex = Math.floor(Math.random() * multipleChoiceQuestions.length);
    const question = multipleChoiceQuestions[randomIndex];
    return {
        text: question.text,
        options: question.options,
        correctAnswer: question.correct,
        type: 'choice'
    };
}

